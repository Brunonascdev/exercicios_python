#Exercício 5: Escreva um programa que leia a idade de uma pessoa e
#deverá ser exibido na tela, se esta pessoa é maior ou menor de idade
#(considerar 18 anos para maior idade).
#(Dica: Usar operadores ternários)

age = int(input("Insira sua idade: "))

if age >= 18:
    print("Você é maior!")
else:
    print("Você é menor!")
        


        
          
